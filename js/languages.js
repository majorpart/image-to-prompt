// Get current language from localStorage or default to English
function getCurrentLanguage() {
    return localStorage.getItem('selectedLanguage') || 'en';
}

// Set current language
function setCurrentLanguage(lang) {
    localStorage.setItem('selectedLanguage', lang);
    updateLanguageDisplay(lang);
    translatePage(lang);
    updateDocumentDirection(lang);
}

// Update language display in UI
function updateLanguageDisplay(lang) {
    const config = languageConfig[lang];
    const languageBtn = document.getElementById('languageBtn');
    if (languageBtn) {
        languageBtn.innerHTML = `<span class="text-sm">${config.flag} ${config.name.substring(0, 2).toUpperCase()}</span>
            <svg class="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M19 9l-7 7-7-7"></path>
            </svg>`;
    }
}

// Update document direction for RTL languages
function updateDocumentDirection(lang) {
    const config = languageConfig[lang];
    document.documentElement.dir = config.dir;
    document.documentElement.lang = lang;
}

// Translate page content
function translatePage(lang) {
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            element.textContent = translations[lang][key];
        } else if (translations['en'] && translations['en'][key]) {
            // Fallback to English
            element.textContent = translations['en'][key];
        }
    });
    
    // Regenerate testimonials with new language
    if (window.imageToPromptApp && window.imageToPromptApp.generateTestimonials) {
        window.imageToPromptApp.generateTestimonials();
    }
}

// Get translated text
function getTranslation(key, lang = null) {
    const currentLang = lang || getCurrentLanguage();
    return translations[currentLang]?.[key] || translations['en']?.[key] || key;
}

// Get all translations for a specific language
function getTranslations(lang = null) {
    const currentLang = lang || getCurrentLanguage();
    return translations[currentLang] || translations['en'] || {};
}

// Initialize language system
function initLanguageSystem() {
    const currentLang = getCurrentLanguage();
    updateLanguageDisplay(currentLang);
    translatePage(currentLang);
    updateDocumentDirection(currentLang);
}

// Export functions for use in main.js
window.LanguageSystem = {
    getCurrentLanguage,
    setCurrentLanguage,
    getTranslation,
    getTranslations,
    initLanguageSystem,
    languageConfig
};
